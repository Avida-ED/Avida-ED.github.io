@dataclasses.dataclass
class InventoryItem:
    relpath: str
    ext: str
    size_bytes: int
    mtime: float


@dataclasses.dataclass
class ExtractedItem:
    source_relpath: str
    extracted_relpath: str
    text_hash: str
    title_guess: str
    links: List[str]


_LINK_RE = re.compile(r"https?://[^\s\)]+", re.IGNORECASE)


def extract_links(text: str) -> List[str]:
    return sorted(set(_LINK_RE.findall(text)))


def guess_title_from_text(text: str, fallback: str) -> str:
    for line in text.splitlines():
        s = line.strip()
        if not s:
            continue
        if s.startswith("#"):
            return s.lstrip("#").strip()[:120] or fallback
        return s[:120]
    return fallback


def inventory_legacy(cfg: AppConfig, constraints: Constraints, max_items: int = 0) -> List[InventoryItem]:
    items: List[InventoryItem] = []
    for dirpath, _, filenames in os.walk(cfg.legacy_root):
        for fn in filenames:
            p = Path(dirpath) / fn
            rel = safe_relpath(p.relative_to(cfg.legacy_root))

            # global excludes from config + constraints
            if any(fnmatch.fnmatch(rel, pat) for pat in cfg.exclude_globs):
                continue
            if any(fnmatch.fnmatch(rel, pat) for pat in constraints.global_exclude_patterns):
                continue
            if not any(fnmatch.fnmatch(rel, pat) for pat in cfg.include_globs):
                continue

            st = p.stat()
            items.append(InventoryItem(relpath=rel, ext=p.suffix.lower(), size_bytes=st.st_size, mtime=st.st_mtime))
            if max_items and len(items) >= max_items:
                return items
    return items


def pandoc_to_markdown(cfg: AppConfig, src: Path, media_dir: Path) -> str:
    # Convert to GitHub-flavored Markdown; extract media to a staging dir
    cmd = [
        cfg.tools["pandoc"],
        str(src),
        "-t", "gfm",
        "--wrap=none",
        "--extract-media", str(media_dir),
    ]
    rc, out, err = run_cmd(cmd, cwd=cfg.legacy_root, timeout=300)
    if rc != 0:
        raise RuntimeError(f"pandoc failed ({rc}) for {src}:\n{err}")
    return out


def pdftotext_extract(cfg: AppConfig, src: Path) -> str:
    cmd = [cfg.tools["pdftotext"], "-layout", str(src), "-"]
    rc, out, err = run_cmd(cmd, cwd=cfg.legacy_root, timeout=300)
    if rc != 0:
        raise RuntimeError(f"pdftotext failed ({rc}) for {src}:\n{err}")
    return out


def extract_one(cfg: AppConfig, item: InventoryItem) -> ExtractedItem:
    src = cfg.legacy_root / item.relpath
    extracted_dir = cfg.output_dir / "extracted"
    extracted_dir.mkdir(parents=True, exist_ok=True)

    out_name = item.relpath.replace("/", "__") + ".md"
    extracted_path = extracted_dir / out_name

    data = src.read_bytes()
    src_hash = sha256_bytes(data)
    cache_key = sha256_text(f"extract:v2.1:{item.relpath}:{src_hash}")
    cache_file = cfg.cache_dir / "extract" / f"{cache_key}.json"

    if cache_file.exists() and extracted_path.exists():
        cached = json.loads(read_text(cache_file))
        return ExtractedItem(**cached)

    media_dir = extracted_dir / "media" / out_name.replace(".md", "")
    media_dir.mkdir(parents=True, exist_ok=True)

    if item.ext in [".html", ".htm", ".docx", ".odt", ".rtf"]:
        md = pandoc_to_markdown(cfg, src, media_dir)
    elif item.ext in [".md", ".txt"]:
        md = read_text(src)
    elif item.ext == ".pdf":
        md = pdftotext_extract(cfg, src)
    else:
        md = read_text(src)

    title_guess = guess_title_from_text(md, fallback=Path(item.relpath).stem)
    links = extract_links(md)
    text_hash = sha256_text(md)

    write_text(extracted_path, md)

    result = ExtractedItem(
        source_relpath=item.relpath,
        extracted_relpath=safe_relpath(extracted_path.relative_to(cfg.output_dir)),
        text_hash=text_hash,
        title_guess=title_guess,
        links=links,
    )

    cache_file.parent.mkdir(parents=True, exist_ok=True)
    write_text(cache_file, json.dumps(dataclasses.asdict(result), indent=2))
    return result


