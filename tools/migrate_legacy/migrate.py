class OpenAICompatClient:
    def __init__(self, base_url: str, api_key: str, model: str):
        self.base_url = base_url.rstrip("/")
        self.api_key = api_key
        self.model = model

    def chat(self, system: str, user: str, temperature: float, max_tokens: int, timeout_s: int) -> str:
        url = f"{self.base_url}/chat/completions"
        headers = {"Authorization": f"Bearer {self.api_key}"}
        payload = {
            "model": self.model,
            "temperature": temperature,
            "max_tokens": max_tokens,
            "messages": [
                {"role": "system", "content": system},
                {"role": "user", "content": user},
            ],
        }
        r = requests.post(url, headers=headers, json=payload, timeout=timeout_s)
        r.raise_for_status()
        data = r.json()
        return data["choices"][0]["message"]["content"]


def make_llm(cfg: AppConfig) -> OpenAICompatClient:
    llm = cfg.llm
    if llm.get("backend") != "openai_compat":
        raise ValueError("v2.1 expects llm.backend=openai_compat (vLLM or llamafile OpenAI-compatible server).")
    return OpenAICompatClient(
        base_url=llm["openai_compat_url"],
        api_key=llm.get("openai_compat_api_key", "local-no-key"),
        model=llm["model"],
    )


def cached_llm(cfg: AppConfig, namespace: str, key_material: str, call_fn) -> str:
    key = sha256_text(key_material)
    p = cfg.cache_dir / "llm" / namespace / f"{key}.txt"
    if p.exists():
        return read_text(p)
    out = call_fn()
    p.parent.mkdir(parents=True, exist_ok=True)
    write_text(p, out)
    return out


