def canonical_destinations(cfg: AppConfig) -> Dict[str, str]:
    fw = cfg.first_wave
    return {
        "get_started.index": fw["get_started"]["index"],
        "get_started.choose_version": fw["get_started"]["choose_version"],
        "get_started.system_requirements": fw["get_started"]["system_requirements"],
        "get_started.first_experiment": fw["get_started"]["first_experiment"],
        "get_started.troubleshooting": fw["get_started"]["troubleshooting"],
        "teachers.index": fw["teachers"]["index"],
        "teachers.quick_start": fw["teachers"]["quick_start"],
        "students.index": fw["students"]["index"],
        "students.first_steps": fw["students"]["first_steps"],
        "support.index": fw["support"]["index"],
        "support.accessibility": fw["support"]["accessibility"],
        "about.index": fw["about"]["index"],
        "about.how_to_cite": fw["about"]["how_to_cite"],
    }


def validate_dest(cfg: AppConfig, dest: str) -> None:
    if not any(dest.startswith(prefix) for prefix in cfg.allowed_dest_prefixes):
        raise ValueError(f"Invalid dest outside allowed prefixes: {dest}")


def is_archive_candidate(constraints: Constraints, relpath: str) -> bool:
    return any(fnmatch.fnmatch(relpath, pat) for pat in constraints.global_archive_patterns)


def filter_candidates_for_page(
    constraints: Constraints,
    page_type: str,
    extracted: List[ExtractedItem],
) -> List[ExtractedItem]:
    forbid = constraints.forbid_patterns(page_type)
    prefer = constraints.prefer_patterns(page_type)
    forced = set(constraints.force_include(page_type))

    # Start: exclude archive candidates if we're planning a non-archive page
    base = []
    for e in extracted:
        if e.source_relpath in forced:
            base.append(e)
            continue
        # Deprioritize archive-y content in planning by excluding; forced can override
        if is_archive_candidate(constraints, e.source_relpath):
            continue
        # Forbid patterns
        if any(fnmatch.fnmatch(e.source_relpath, pat) for pat in forbid):
            continue
        base.append(e)

    # If prefer patterns exist, keep all but we will sort with prefers first
    def pref_score(e: ExtractedItem) -> int:
        if e.source_relpath in forced:
            return 100
        if any(fnmatch.fnmatch(e.source_relpath.lower(), pat.lower()) for pat in prefer):
            return 10
        # mild heuristic: teacher/student tokens in title/path
        s = (e.source_relpath + " " + e.title_guess).lower()
        if page_type.startswith("teachers.") and "teacher" in s:
            return 5
        if page_type.startswith("students.") and "student" in s:
            return 5
        if page_type.startswith("get_started.") and ("start" in s or "install" in s or "launch" in s):
            return 5
        return 0

    base.sort(key=pref_score, reverse=True)
    return base


def plan_pages(
    cfg: AppConfig,
    llm: OpenAICompatClient,
    constraints: Constraints,
    extracted: List[ExtractedItem],
    page_types: List[str],
) -> List[Dict[str, Any]]:
    sys_prompt = load_prompt("system.md")
    planner_prompt = load_prompt("planner.md")
    schema = load_schema("page_plan.schema.json")
    canon = canonical_destinations(cfg)

    plans: List[Dict[str, Any]] = []
    for page_type in page_types:
        if page_type not in canon:
            continue

        dest = constraints.dest_override(page_type) or canon[page_type]
        validate_dest(cfg, dest)

        candidates = filter_candidates_for_page(constraints, page_type, extracted)

        # Build candidate summaries payload; if doc summary missing, include minimal fallback
        candidate_payload = []
        for e in candidates:
            ds = load_doc_summary(cfg, e.source_relpath)
            candidate_payload.append({
                "source": e.source_relpath,
                "title_guess": e.title_guess,
                "doc_summary": ds if ds is not None else {
                    "doc_title_guess": e.title_guess,
                    "audience": "unknown",
                    "content_type": "unknown",
                    "best_use": "unknown",
                    "key_points": [],
                    "still_relevant": [],
                    "likely_outdated": [],
                    "notable_links": e.links[:10],
                }
            })

        forced = constraints.force_include(page_type)
        forbid = constraints.forbid_patterns(page_type)

        user = (
            f"{planner_prompt}\n\n"
            f"Target page_type: {page_type}\n"
            f"Destination (repo-relative): {dest}\n\n"
            f"Constraints:\n"
            f"- force_include: {json.dumps(forced)}\n"
            f"- forbid_patterns: {json.dumps(forbid)}\n\n"
            f"Candidates (with doc_summary):\n{json.dumps(candidate_payload, indent=2)[:200000]}\n\n"
            f"Return JSON only."
        )

        key_material = (
            f"plan:v2.1:{cfg.llm['model']}:{page_type}:"
            f"{sha256_text(sys_prompt)}:{sha256_text(planner_prompt)}:{sha256_text(user)}"
        )

        raw = cached_llm(cfg, "plan", key_material, lambda: llm.chat(
            system=sys_prompt,
            user=user,
            temperature=float(cfg.llm.get("temperature", 0.2)),
            max_tokens=1800,
            timeout_s=int(cfg.llm.get("timeout_s", 120)),
        ))

        obj = parse_first_json_object(raw)
        jsonschema_validate(instance=obj, schema=schema)

        # Enforce page_type + dest
        obj["page_type"] = page_type
        obj["dest"] = dest
        obj["page_id"] = obj.get("page_id") or page_type.replace(".", "_")

        # Enforce forced includes if present in extracted
        available_sources = set(e.source_relpath for e in extracted)
        forced_present = [s for s in forced if s in available_sources]
        sf = list(dict.fromkeys(obj.get("source_files", [])))

        # Remove forbidden patterns post-hoc
        def allowed_source(s: str) -> bool:
            return not any(fnmatch.fnmatch(s, pat) for pat in forbid)

        sf = [s for s in sf if allowed_source(s)]
        for s in forced_present:
            if s not in sf:
                sf.insert(0, s)

        if not sf:
            # fail safe: if nothing left, create a minimal plan with forced only or mark warnings
            sf = forced_present[:]
            obj["warnings"] = list(dict.fromkeys(obj.get("warnings", []) + ["Planner returned no usable sources; using forced sources only."]))

        obj["source_files"] = sf

        so = [s for s in obj.get("source_order", []) if s in sf]
        if not so:
            so = sf[:]
        obj["source_order"] = so

        # Required sections must exist
        if "required_sections" not in obj or not isinstance(obj["required_sections"], list):
            obj["required_sections"] = []

        plans.append(obj)

    return plans


