def summarize_chunk(cfg: AppConfig, llm: OpenAICompatClient, chunk: ChunkItem) -> Dict[str, Any]:
    sys_prompt = load_prompt("system.md")
    user_prompt = load_prompt("summarize_chunk.md")
    schema = load_schema("chunk_summary.schema.json")

    chunk_text = read_text(cfg.output_dir / chunk.chunk_relpath)

    key_material = (
        f"sum_chunk:v2.1:{cfg.llm['model']}:{sha256_text(sys_prompt)}:{sha256_text(user_prompt)}:{chunk.chunk_hash}"
    )

    raw = cached_llm(cfg, "sum_chunk", key_material, lambda: llm.chat(
        system=sys_prompt,
        user=f"{user_prompt}\n\nCHUNK:\n---\n{chunk_text}\n---\n\nReturn JSON only.",
        temperature=float(cfg.summarization.get("temperature", 0.1)),
        max_tokens=int(cfg.summarization.get("chunk_summary_tokens", 350)),
        timeout_s=int(cfg.llm.get("timeout_s", 120)),
    ))

    obj = parse_first_json_object(raw)
    jsonschema_validate(instance=obj, schema=schema)

    out_path = cfg.output_dir / "summaries" / "chunk" / chunk.source_id / f"chunk_{chunk.chunk_index:03d}.json"
    write_text(out_path, json.dumps(obj, indent=2))
    return obj


def summarize_doc(cfg: AppConfig, llm: OpenAICompatClient, extracted: ExtractedItem, chunk_summaries: List[Dict[str, Any]]) -> Dict[str, Any]:
    sys_prompt = load_prompt("system.md")
    user_prompt = load_prompt("summarize_doc.md")
    schema = load_schema("doc_summary.schema.json")

    payload = json.dumps(chunk_summaries, indent=2)
    key_material = (
        f"sum_doc:v2.1:{cfg.llm['model']}:{sha256_text(sys_prompt)}:{sha256_text(user_prompt)}:{sha256_text(payload)}"
    )

    raw = cached_llm(cfg, "sum_doc", key_material, lambda: llm.chat(
        system=sys_prompt,
        user=f"{user_prompt}\n\nChunk summaries JSON:\n{payload}\n\nReturn JSON only.",
        temperature=float(cfg.summarization.get("temperature", 0.1)),
        max_tokens=int(cfg.summarization.get("doc_summary_tokens", 450)),
        timeout_s=int(cfg.llm.get("timeout_s", 120)),
    ))

    obj = parse_first_json_object(raw)
    jsonschema_validate(instance=obj, schema=schema)

    source_id = make_source_id(extracted.source_relpath)
    out_path = cfg.output_dir / "summaries" / "doc" / f"{source_id}.json"
    write_text(out_path, json.dumps(obj, indent=2))
    return obj


def load_doc_summary(cfg: AppConfig, source_relpath: str) -> Optional[Dict[str, Any]]:
    source_id = make_source_id(source_relpath)
    p = cfg.output_dir / "summaries" / "doc" / f"{source_id}.json"
    if not p.exists():
        return None
    return json.loads(read_text(p))


