_MD_LINK = re.compile(r"\[([^\]]+)\]\(([^)]+)\)")


def build_linkmap(plans: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Conservative deterministic mapping:
    legacy source file path -> new doc path (dest) for sources included in plans.
    """
    entries = []
    assigned: Dict[str, str] = {}
    for plan in plans:
        dest = plan["dest"]
        for src in plan["source_files"]:
            assigned.setdefault(src, dest)

    for legacy_src, new_dest in assigned.items():
        entries.append({"legacy_ref": legacy_src, "new_ref": "/" + new_dest.replace("docs/", "docs/"), "confidence": 0.95})

    return {"generated_at": now_iso(), "entries": entries}


def rewrite_links_in_markdown(md: str, linkmap: Dict[str, Any]) -> Tuple[str, List[str]]:
    warnings: List[str] = []
    lookup: Dict[str, str] = {}
    for e in linkmap.get("entries", []):
        legacy = e["legacy_ref"]
        new_ref = e["new_ref"]
        lookup[legacy] = new_ref
        lookup[legacy.lstrip("./")] = new_ref
        lookup[legacy.replace("\\", "/")] = new_ref

    def repl(m):
        text, target = m.group(1), m.group(2).strip()
        anchor = ""
        tnorm = target
        if "#" in tnorm:
            tnorm, a = tnorm.split("#", 1)
            anchor = "#" + a
        key = tnorm.lstrip("./")
        if key in lookup:
            return f"[{text}]({lookup[key]}{anchor})"
        return m.group(0)

    patched = _MD_LINK.sub(repl, md)

    # Heuristic warnings
    if ".html" in patched or ".htm" in patched:
        warnings.append("Some legacy .html links remain; review for manual mapping.")
    if "click here" in patched.lower():
        warnings.append("Found non-descriptive link text ('click here'); revise.")
    return patched, warnings


