def load_chunk_items_for_source(cfg: AppConfig, source_relpath: str) -> List[ChunkItem]:
    source_id = make_source_id(source_relpath)
    chunks_dir = cfg.output_dir / "chunks" / source_id
    if not chunks_dir.exists():
        return []
    items = []
    for p in sorted(chunks_dir.glob("chunk_*.md")):
        idx = int(p.stem.split("_")[1])
        txt = read_text(p)
        items.append(ChunkItem(
            source_relpath=source_relpath,
            source_id=source_id,
            chunk_index=idx,
            chunk_relpath=safe_relpath(p.relative_to(cfg.output_dir)),
            chunk_hash=sha256_text(txt),
        ))
    return items


def pick_evidence_chunks(cfg: AppConfig, plan: Dict[str, Any], source_relpath: str, k: int = 2) -> List[ChunkItem]:
    """
    Deterministic evidence selection:
    - prefer first chunk
    - plus one chunk with keyword overlap (if possible), else second chunk
    """
    chunks = load_chunk_items_for_source(cfg, source_relpath)
    if not chunks:
        return []

    required_sections = " ".join(plan.get("required_sections", [])).lower()
    keywords = set(re.findall(r"[a-zA-Z]{4,}", required_sections))  # crude but stable

    def score(chunk: ChunkItem) -> int:
        txt = read_text(cfg.output_dir / chunk.chunk_relpath).lower()
        return sum(1 for kw in keywords if kw in txt)

    ranked = sorted(chunks, key=score, reverse=True)
    chosen = []
    chosen.append(chunks[0])
    if k > 1:
        best = ranked[0]
        if best.chunk_index != chunks[0].chunk_index:
            chosen.append(best)
        elif len(chunks) > 1:
            chosen.append(chunks[1])
    return chosen[:k]


