@dataclasses.dataclass
class ChunkItem:
    source_relpath: str
    source_id: str
    chunk_index: int
    chunk_relpath: str
    chunk_hash: str


_HEADING_RE = re.compile(r"^\s{0,3}#{1,6}\s+.+$", re.MULTILINE)


def make_source_id(source_relpath: str) -> str:
    # stable identifier for paths
    return sha256_text("srcid:" + source_relpath)[:16]


def split_by_headings(text: str) -> List[str]:
    """
    If markdown-ish headings exist, split into sections at headings.
    Keep headings with their following content.
    """
    matches = list(_HEADING_RE.finditer(text))
    if len(matches) < 2:
        return [text]

    chunks = []
    for i, m in enumerate(matches):
        start = m.start()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        section = text[start:end].strip()
        if section:
            chunks.append(section)
    return chunks or [text]


def chunk_text_fixed(text: str, chunk_chars: int, overlap_chars: int) -> List[str]:
    if len(text) <= chunk_chars:
        return [text]
    chunks = []
    start = 0
    while start < len(text):
        end = min(len(text), start + chunk_chars)
        chunk = text[start:end]
        chunks.append(chunk)
        if end >= len(text):
            break
        start = max(0, end - overlap_chars)
    return chunks


def chunk_extracted(cfg: AppConfig, extracted: ExtractedItem) -> List[ChunkItem]:
    extracted_text = read_text(cfg.output_dir / extracted.extracted_relpath)
    source_id = make_source_id(extracted.source_relpath)

    chunk_chars = int(cfg.chunking.get("chunk_chars", 7000))
    overlap_chars = int(cfg.chunking.get("overlap_chars", 500))
    max_chunks = int(cfg.chunking.get("max_chunks_per_doc", 30))

    # heading split first, then size-normalize
    sections = split_by_headings(extracted_text)
    normalized: List[str] = []
    for sec in sections:
        if len(sec) > chunk_chars * 1.2:
            normalized.extend(chunk_text_fixed(sec, chunk_chars, overlap_chars))
        else:
            normalized.append(sec)

    # cap
    if len(normalized) > max_chunks:
        normalized = normalized[:max_chunks]

    out_dir = cfg.output_dir / "chunks" / source_id
    out_dir.mkdir(parents=True, exist_ok=True)

    chunk_items: List[ChunkItem] = []
    for idx, chunk in enumerate(normalized):
        chunk_hash = sha256_text(chunk)
        chunk_file = out_dir / f"chunk_{idx:03d}.md"
        write_text(chunk_file, chunk)
        chunk_items.append(
            ChunkItem(
                source_relpath=extracted.source_relpath,
                source_id=source_id,
                chunk_index=idx,
                chunk_relpath=safe_relpath(chunk_file.relative_to(cfg.output_dir)),
                chunk_hash=chunk_hash,
            )
        )
    return chunk_items


