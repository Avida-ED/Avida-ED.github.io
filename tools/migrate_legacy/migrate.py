@dataclasses.dataclass
class AppConfig:
    legacy_root: Path
    target_repo_root: Path
    output_dir: Path
    cache_dir: Path

    allowed_dest_prefixes: List[str]
    include_globs: List[str]
    exclude_globs: List[str]

    tools: Dict[str, str]
    llm: Dict[str, Any]
    pipeline: Dict[str, Any]
    first_wave: Dict[str, Any]

    chunking: Dict[str, Any]
    summarization: Dict[str, Any]

    constraints_file: Optional[Path]


def load_config(config_path: Path) -> AppConfig:
    raw = yaml.safe_load(read_text(config_path))

    legacy_root = (config_path.parent / raw["legacy_root"]).resolve()
    target_repo_root = (config_path.parent / raw["target_repo_root"]).resolve()
    output_dir = (config_path.parent / raw["output_dir"]).resolve()
    cache_dir = (config_path.parent / raw["cache_dir"]).resolve()

    constraints_file = None
    if "constraints_file" in raw and raw["constraints_file"]:
        constraints_file = (config_path.parent / raw["constraints_file"]).resolve()

    chunking = raw.get("chunking", {"chunk_chars": 7000, "overlap_chars": 500, "max_chunks_per_doc": 30})
    summarization = raw.get("summarization", {"chunk_summary_tokens": 350, "doc_summary_tokens": 450, "temperature": 0.1})

    return AppConfig(
        legacy_root=legacy_root,
        target_repo_root=target_repo_root,
        output_dir=output_dir,
        cache_dir=cache_dir,
        allowed_dest_prefixes=raw["allowed_dest_prefixes"],
        include_globs=raw["include_globs"],
        exclude_globs=raw["exclude_globs"],
        tools=raw["tools"],
        llm=raw["llm"],
        pipeline=raw["pipeline"],
        first_wave=raw["first_wave"],
        chunking=chunking,
        summarization=summarization,
        constraints_file=constraints_file,
    )


@dataclasses.dataclass
class Constraints:
    global_strict_allowlist: bool
    global_exclude_patterns: List[str]
    global_archive_patterns: List[str]
    pages: Dict[str, Any]

    @staticmethod
    def load(path: Optional[Path]) -> "Constraints":
        if not path:
            return Constraints(False, [], [], {})
        raw = yaml.safe_load(read_text(path))
        g = raw.get("global", {})
        return Constraints(
            global_strict_allowlist=bool(g.get("strict_allowlist", False)),
            global_exclude_patterns=list(g.get("exclude_patterns", [])),
            global_archive_patterns=list(g.get("archive_patterns", [])),
            pages=dict(raw.get("pages", {})),
        )

    def page_cfg(self, page_type: str) -> Dict[str, Any]:
        return self.pages.get(page_type, {})

    def force_include(self, page_type: str) -> List[str]:
        return list(self.page_cfg(page_type).get("force_include", []))

    def prefer_patterns(self, page_type: str) -> List[str]:
        return list(self.page_cfg(page_type).get("prefer_include_patterns", []))

    def forbid_patterns(self, page_type: str) -> List[str]:
        return list(self.page_cfg(page_type).get("forbid_patterns", []))

    def dest_override(self, page_type: str) -> Optional[str]:
        v = self.page_cfg(page_type).get("dest_override")
        return str(v) if v else None


