def can_overwrite(dest: Path) -> bool:
    if not dest.exists():
        return True
    head = read_text(dest)[:5000]
    return GENERATED_MARKER in head


def apply_page(cfg: AppConfig, draft: Dict[str, Any], dry_run: bool, overwrite: bool) -> Path:
    dest_path = cfg.target_repo_root / draft["dest"]

    if dest_path.exists():
        if not overwrite:
            raise FileExistsError(f"Refusing overwrite (overwrite=false): {dest_path}")
        if not can_overwrite(dest_path):
            raise PermissionError(f"Refusing overwrite: missing generated marker: {dest_path}")

    if dry_run:
        return dest_path

    write_text(dest_path, draft["markdown"])
    return dest_path


