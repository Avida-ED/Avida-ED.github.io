def parse_csv_list(s: str) -> List[str]:
    s = (s or "").strip()
    if not s:
        return []
    return [x.strip() for x in s.split(",") if x.strip()]


def load_jsonl(path: Path) -> List[Dict[str, Any]]:
    if not path.exists():
        return []
    return [json.loads(line) for line in read_text(path).splitlines() if line.strip()]


def save_jsonl(path: Path, rows: List[Dict[str, Any]]) -> None:
    write_text(path, "\n".join(json.dumps(r) for r in rows) + ("\n" if rows else ""))


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--config", default=str(HERE / "config.yaml"))
    ap.add_argument("--max-items", type=int, default=0)
    ap.add_argument("--page-types", default="", help="Comma-separated list of page_types to run (subset).")

    sub = ap.add_subparsers(dest="cmd", required=True)
    sub.add_parser("inventory")
    sub.add_parser("extract")
    sub.add_parser("chunk")
    sub.add_parser("summarize")
    sub.add_parser("plan")
    sub.add_parser("draft")
    sub.add_parser("linkmap")
    sub.add_parser("repair-links")
    sub.add_parser("apply")
    sub.add_parser("qa")

    p_run = sub.add_parser("run")
    p_run.add_argument("--dry-run", default="true")
    p_run.add_argument("--overwrite", default="false")

    args = ap.parse_args()

    cfg = load_config(Path(args.config))
    constraints = Constraints.load(cfg.constraints_file)

    cfg.output_dir.mkdir(parents=True, exist_ok=True)
    cfg.cache_dir.mkdir(parents=True, exist_ok=True)

    llm = make_llm(cfg)

    max_items = args.max_items or int(cfg.pipeline.get("max_items", 0) or 0)
    page_types_filter = set(parse_csv_list(args.page_types))

    inv_path = cfg.output_dir / "inventory.jsonl"
    ext_idx_path = cfg.output_dir / "extracted_index.jsonl"
    chunks_idx_path = cfg.output_dir / "chunks_index.jsonl"
    plans_path = cfg.output_dir / "plans.jsonl"
    drafts_path = cfg.output_dir / "drafts_index.jsonl"
    linkmap_path = cfg.output_dir / "linkmap.json"
    report_path = cfg.output_dir / "report.md"

    notes: List[str] = []

    # Determine page_types to target (canonical list, optionally filtered)
    canon = canonical_destinations(cfg)
    target_page_types = list(canon.keys())
    if page_types_filter:
        target_page_types = [pt for pt in target_page_types if pt in page_types_filter]

    # -------------------------
    # Stage: inventory
    # -------------------------
    if args.cmd in ("inventory", "run"):
        inv_items = inventory_legacy(cfg, constraints, max_items=max_items)
        save_jsonl(inv_path, [dataclasses.asdict(i) for i in inv_items])
    else:
        inv_items = [InventoryItem(**r) for r in load_jsonl(inv_path)]

    # -------------------------
    # Stage: extract
    # -------------------------
    if args.cmd in ("extract", "run"):
        extracted_items: List[ExtractedItem] = []
        for i in inv_items[: max_items or len(inv_items)]:
            try:
                extracted_items.append(extract_one(cfg, i))
            except Exception as e:
                notes.append(f"EXTRACT FAIL: {i.relpath}: {e}")
        save_jsonl(ext_idx_path, [dataclasses.asdict(e) for e in extracted_items])
    else:
        extracted_items = [ExtractedItem(**r) for r in load_jsonl(ext_idx_path)]

    # -------------------------
    # Stage: chunk
    # -------------------------
    if args.cmd in ("chunk", "run"):
        chunk_items: List[ChunkItem] = []
        for e in extracted_items[: max_items or len(extracted_items)]:
            try:
                chunk_items.extend(chunk_extracted(cfg, e))
            except Exception as ex:
                notes.append(f"CHUNK FAIL: {e.source_relpath}: {ex}")
        save_jsonl(chunks_idx_path, [dataclasses.asdict(c) for c in chunk_items])
    else:
        chunk_items = [ChunkItem(**r) for r in load_jsonl(chunks_idx_path)]

    # Group chunks by source_id
    chunks_by_source: Dict[str, List[ChunkItem]] = {}
    for c in chunk_items:
        chunks_by_source.setdefault(c.source_id, []).append(c)
    for k in list(chunks_by_source.keys()):
        chunks_by_source[k].sort(key=lambda x: x.chunk_index)

    # -------------------------
    # Stage: summarize (chunk + doc)
    # -------------------------
    if args.cmd in ("summarize", "run"):
        # Summarize each chunk, then summarize docs
        extracted_by_src = {e.source_relpath: e for e in extracted_items}

        for e in extracted_items[: max_items or len(extracted_items)]:
            src = e.source_relpath
            source_id = make_source_id(src)
            chunks = chunks_by_source.get(source_id, [])
            if not chunks:
                notes.append(f"SUMMARIZE SKIP: no chunks for {src}")
                continue

            chunk_summaries: List[Dict[str, Any]] = []
            for ch in chunks:
                # If already written, load it (avoid re-LLM on reruns)
                sp = cfg.output_dir / "summaries" / "chunk" / ch.source_id / f"chunk_{ch.chunk_index:03d}.json"
                if sp.exists():
                    chunk_summaries.append(json.loads(read_text(sp)))
                    continue
                try:
                    chunk_summaries.append(summarize_chunk(cfg, llm, ch))
                except Exception as ex:
                    notes.append(f"SUMMARIZE CHUNK FAIL: {src} chunk {ch.chunk_index}: {ex}")

            # Doc summary
            dp = cfg.output_dir / "summaries" / "doc" / f"{source_id}.json"
            if not dp.exists():
                try:
                    summarize_doc(cfg, llm, e, chunk_summaries)
                except Exception as ex:
                    notes.append(f"SUMMARIZE DOC FAIL: {src}: {ex}")
    else:
        # no-op: summaries are loaded on demand
        pass

    # -------------------------
    # Stage: plan (many-to-one)
    # -------------------------
    if args.cmd in ("plan", "run"):
        try:
            plans = plan_pages(cfg, llm, constraints, extracted_items, target_page_types)
            save_jsonl(plans_path, plans)
        except Exception as ex:
            notes.append(f"PLAN FAIL: {ex}")
            plans = []
    else:
        plans = load_jsonl(plans_path)

    # -------------------------
    # Stage: draft
    # -------------------------
    if args.cmd in ("draft", "run"):
        schema = load_schema("page_draft.schema.json")
        drafts: List[Dict[str, Any]] = []

        # Filter plans by page_types filter if set
        plans_to_run = plans
        if page_types_filter:
            plans_to_run = [p for p in plans if p.get("page_type") in page_types_filter]

        for plan in plans_to_run:
            try:
                d = draft_from_plan(cfg, llm, plan)
                jsonschema_validate(instance=d, schema=schema)
                drafts.append(d)
            except Exception as ex:
                notes.append(f"DRAFT FAIL: {plan.get('dest')}: {ex}")

        save_jsonl(drafts_path, drafts)
    else:
        drafts = load_jsonl(drafts_path)

    # -------------------------
    # Stage: linkmap
    # -------------------------
    if args.cmd in ("linkmap", "run"):
        lm = build_linkmap(plans)
        write_text(linkmap_path, json.dumps(lm, indent=2))
    else:
        lm = json.loads(read_text(linkmap_path)) if linkmap_path.exists() else {"generated_at": now_iso(), "entries": []}

    # -------------------------
    # Stage: repair-links
    # -------------------------
    if args.cmd in ("repair-links", "run"):
        patched: List[Dict[str, Any]] = []
        for d in drafts:
            md, warns = rewrite_links_in_markdown(d["markdown"], lm)
            d2 = dict(d)
            d2["markdown"] = md
            d2["warnings"] = list(dict.fromkeys(d2.get("warnings", []) + warns))
            patched.append(d2)
        drafts = patched
        save_jsonl(drafts_path, drafts)

    # -------------------------
    # Stage: apply
    # -------------------------
    if args.cmd in ("apply", "run"):
        dry_run = (getattr(args, "dry_run", "true").lower() == "true")
        overwrite = (getattr(args, "overwrite", "false").lower() == "true")
        applied = 0

        # Filter drafts by page_types filter if set
        drafts_to_apply = drafts
        if page_types_filter:
            drafts_to_apply = [d for d in drafts if d.get("dest") and any(d.get("dest","").startswith(canon[pt].split("/")[0]) for pt in page_types_filter) ]

        for d in drafts_to_apply:
            try:
                apply_page(cfg, d, dry_run=dry_run, overwrite=overwrite)
                applied += 1
            except Exception as ex:
                notes.append(f"APPLY FAIL: {d.get('dest')}: {ex}")

        notes.append(f"{'Dry run: would write' if dry_run else 'Wrote'} {applied} pages.")
        if cfg.pipeline.get("run_qa", False) and not dry_run:
            try:
                run_qa(cfg)
            except Exception as ex:
                notes.append(f"QA FAIL: {ex}")

    if args.cmd == "qa":
        run_qa(cfg)

    # -------------------------
    # Report
    # -------------------------
    lines: List[str] = []
    lines.append("# Migration report (v2.1)\n\n")
    lines.append(f"- Generated: `{now_iso()}`\n")
    lines.append(f"- Legacy root: `{cfg.legacy_root}`\n")
    lines.append(f"- Target repo root: `{cfg.target_repo_root}`\n\n")

    lines.append("## Counts\n")
    lines.append(f"- inventory: {len(inv_items)}\n")
    lines.append(f"- extracted: {len(extracted_items)}\n")
    lines.append(f"- chunks: {len(chunk_items)}\n")
    lines.append(f"- plans: {len(plans)}\n")
    lines.append(f"- drafts: {len(drafts)}\n\n")

    if notes:
        lines.append("## Notes\n")
        for n in notes:
            lines.append(f"- {n}\n")
        lines.append("\n")

    write_text(report_path, "".join(lines))
    print(f"[ok] report: {report_path}")


if __name__ == "__main__":
    main()
