def draft_from_plan(cfg: AppConfig, llm: OpenAICompatClient, plan: Dict[str, Any]) -> Dict[str, Any]:
    sys_prompt = load_prompt("system.md")
    rw_prompt = load_prompt("rewriter_synthesis.md")
    schema = load_schema("page_draft.schema.json")

    # Build ordered sources: doc summaries always + a couple evidence chunks
    blocks: List[str] = []
    for src in plan["source_order"]:
        ds = load_doc_summary(cfg, src)
        ds_json = json.dumps(ds, indent=2) if ds else "{}"
        blocks.append(f"## SOURCE: {src}\n\nDOC_SUMMARY_JSON:\n{ds_json}\n")
        for ev in pick_evidence_chunks(cfg, plan, src, k=2):
            ev_text = read_text(cfg.output_dir / ev.chunk_relpath)
            blocks.append(f"### EVIDENCE_CHUNK: {src} :: {Path(ev.chunk_relpath).name}\n\n{ev_text}\n")

    user = (
        f"{rw_prompt}\n\n"
        f"PagePlan JSON:\n{json.dumps(plan, indent=2)}\n\n"
        f"Ordered sources with summaries and evidence:\n\n"
        + "\n---\n".join(blocks)
        + "\n\nReturn JSON object first, then the exact Markdown."
    )

    key_material = (
        f"draft:v2.1:{cfg.llm['model']}:{plan['page_type']}:"
        f"{sha256_text(sys_prompt)}:{sha256_text(rw_prompt)}:{sha256_text(user)}"
    )

    raw = cached_llm(cfg, "draft", key_material, lambda: llm.chat(
        system=sys_prompt,
        user=user,
        temperature=float(cfg.llm.get("temperature", 0.2)),
        max_tokens=int(cfg.llm.get("max_tokens", 2500)),
        timeout_s=int(cfg.llm.get("timeout_s", 120)),
    ))

    obj = parse_first_json_object(raw)
    jsonschema_validate(instance=obj, schema=schema)

    # Enforce dest + sources
    obj["dest"] = plan["dest"]
    obj["source_files"] = plan["source_files"]
    obj["generated_marker"] = True

    # Enforce generated marker
    md = obj["markdown"].lstrip()
    if not md.startswith(GENERATED_MARKER):
        md = GENERATED_MARKER + "\n\n" + md
    # Enforce sources comment
    if "<!-- Sources:" not in md:
        md = md.rstrip() + "\n\n<!-- Sources: " + "; ".join(plan["source_files"]) + " -->\n"
    obj["markdown"] = md

    return obj


