def now_iso() -> str:
    return time.strftime("%Y-%m-%dT%H:%M:%S")


def sha256_bytes(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def sha256_text(s: str) -> str:
    return sha256_bytes(s.encode("utf-8", errors="replace"))


def read_text(p: Path) -> str:
    return p.read_text(encoding="utf-8", errors="replace")


def write_text(p: Path, s: str) -> None:
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(s, encoding="utf-8")


def run_cmd(cmd: List[str], cwd: Optional[Path] = None, timeout: int = 300) -> Tuple[int, str, str]:
    proc = subprocess.run(
        cmd,
        cwd=str(cwd) if cwd else None,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        timeout=timeout,
        text=True,
        encoding="utf-8",
        errors="replace",
    )
    return proc.returncode, proc.stdout, proc.stderr


def load_prompt(name: str) -> str:
    p = HERE / "prompts" / name
    if not p.exists():
        raise FileNotFoundError(p)
    return read_text(p)


def load_schema(name: str) -> Dict[str, Any]:
    p = HERE / "schemas" / name
    if not p.exists():
        raise FileNotFoundError(p)
    return json.loads(read_text(p))


def parse_first_json_object(text: str) -> Dict[str, Any]:
    """
    Extract and parse the first JSON object from model output.
    Assumes the model emits JSON object near the start.
    """
    s = text.strip()
    if not s:
        raise ValueError("Empty LLM output")

    if s.startswith("{"):
        depth = 0
        for i, ch in enumerate(s):
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
                if depth == 0:
                    return json.loads(s[: i + 1])

    idx = s.find("{")
    if idx >= 0:
        return parse_first_json_object(s[idx:])

    raise ValueError("Could not locate JSON object in LLM output")


def safe_relpath(p: Path) -> str:
    return str(p).replace("\\", "/")


