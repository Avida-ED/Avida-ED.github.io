#!/usr/bin/env python3
"""
tools/migrate_legacy/migrate.py (v2.1)

Agentic legacy-site migration into a Docusaurus repo structure.

Key features:
- Deterministic extraction (pandoc/pdftotext)
- Deterministic chunking (heading-based + size fallback)
- Cached LLM summarization per chunk + per document
- Constraints-driven many-to-one planning
- Synthesis rewrite per planned page
- Deterministic linkmap + link repair
- Strict overwrite policy via generated-marker line

Backends:
- vLLM (OpenAI-compatible server): POST /v1/chat/completions

Typical workflow:
  python tools/migrate_legacy/migrate.py run --dry-run true --page-types teachers.quick_start,get_started.index
  python tools/migrate_legacy/migrate.py run --dry-run false --overwrite true

Outputs (under out/):
  inventory.jsonl
  extracted_index.jsonl + extracted/*
  chunks_index.jsonl + chunks/<source_id>/chunk_XXX.md
  summaries/chunk/<source_id>/chunk_XXX.json
  summaries/doc/<source_id>.json
  plans.jsonl
  drafts_index.jsonl
  linkmap.json
  report.md

NOTE: This tool is designed to be deterministic + reviewable.
"""

